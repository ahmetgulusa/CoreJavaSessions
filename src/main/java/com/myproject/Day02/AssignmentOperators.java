package com.myproject.Day02;

public class AssignmentOperators {
    // Assignment Operators or ShortHanded Operators

    /**
     * Assign right value to the left
     * Operator	Example	    Equivalent to
     * =	    a = b;	    a = b;
     * +=	    a += b;	    a = a + b;
     * -=	    a -= b;	    a = a - b;
     * =	    a *= b;	    a = a * b;
     * /=	    a /= b;	    a = a / b;
     * %=	    a %= b;	    a = a % b;
     */
    public static void main(String[] args) {
        int m = 0;
        m = m + 2; //m=2
        m = m + 2; //m=4
        m = m + 2; //m=6
        m = m + 2; //m=8
        m = m + 2; // m += 2;
        m = m + 2; // m -= 2;
        m = m + 2;
        m = m - 2;



    }
}
